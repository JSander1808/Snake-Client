package Singelplayer.Singelplayer_Classic.Game;

public class Drop_classic {

    private int x;
    private int y;

    public Drop_classic(int x, int y){
        this.x=x;
        this.y=y;
    }

    public int getY() {
        return y;
    }

    public int getX() {
        return x;
    }

    public void setY(int y) {
        this.y = y;
    }

    public void setX(int x) {
        this.x = x;
    }
}
